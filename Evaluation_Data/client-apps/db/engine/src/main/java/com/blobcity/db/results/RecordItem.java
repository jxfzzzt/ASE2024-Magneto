package com.blobcity.db.results;

/**
 *
 * @author sanketsarang
 */
public class RecordItem {
    private String name;
    private Class type;
    private Object value;
    
    public RecordItem(final String name, final Class type, final Object value) {
        this.name = name;
        this.type = type;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Class getType() {
        return type;
    }

    public void setType(Class type) {
        this.type = type;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }
}
